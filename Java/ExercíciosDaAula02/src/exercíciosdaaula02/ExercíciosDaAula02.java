package exercíciosdaaula02;

public class ExercíciosDaAula02 {

    public static void main(String[] args) {
        float x = 20;
        float y = 45;
        float soma = x + y;
        float subtracao = x - y;
        float multiplicacao = x * y;
        float divisao = x / y;
        
        System.out.println("Aqui está as operações com os números que você colocou: ");
        System.out.println(x + " + " + y + " = " + soma);
        System.out.println(x + " - " + y + " = " + subtracao);
        System.out.println(x + " x " + y + " = " + multiplicacao);
        System.out.printf(x + " / " + y + " = " + divisao);
    }
    
}
