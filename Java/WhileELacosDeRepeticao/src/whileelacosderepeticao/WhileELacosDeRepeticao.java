package whileelacosderepeticao;

public class WhileELacosDeRepeticao {
    public static void main(String[] args) {
        int i = 100;
        
        System.out.println("Aqui estão os números ímpares até 100");
        for(int contador=1;contador <= i; contador=contador+2){
            System.out.println(contador);
        }
        
        
    }
    
}
